export interface MakoToolDefinition<Name extends string = ToolName> {
  name: Name;
  category: ToolDefinitionSummary["category"];
  description: string;
  annotations: ToolAnnotations;
  inputSchema: ZodTypeAny;
  outputSchema: ZodTypeAny;
  execute(input: ToolInput, options: ToolServiceOptions): Promise<unknown>;
}

export function schemaToJson(
  schema: ZodTypeAny,
  name: string,
  options: { useRootRefs?: boolean } = {},
): JsonObject {
  if (options.useRootRefs) {
    return zodToJsonSchema(schema, {
      name,
      $refStrategy: "root",
    }) as JsonObject;
  }

  return zodToJsonSchema(schema, {
    $refStrategy: "none",
  }) as JsonObject;
}

export function withToolHintsSchema(schema: ZodTypeAny): ZodTypeAny {
  if ((schema._def.typeName as string) === "ZodObject") {
    return (schema as z.AnyZodObject).extend(ToolHintsSchema.shape);
  }
  return z.intersection(schema, ToolHintsSchema);
}

