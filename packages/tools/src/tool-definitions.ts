for (const tool of TOOL_DEFINITIONS) {
  TOOL_DEFINITION_MAP.set(tool.name, tool);
}

export function listToolDefinitions(): ToolDefinitionSummary[] {
  return TOOL_DEFINITIONS.map((tool) => ({
    name: tool.name,
    category: tool.category,
    description: tool.description,
    annotations: ToolAnnotationsSchema.parse(tool.annotations),
    inputSchema: schemaToJson(tool.inputSchema, `${tool.name}Input`, {
      useRootRefs: tool.category === "artifact",
    }),
    outputSchema: schemaToJson(withToolHintsSchema(tool.outputSchema), `${tool.name}Output`, {
      useRootRefs: tool.category === "artifact",
    }),
  }));
}

export function getToolDefinition(name: string): MakoToolDefinition<string> | undefined {
  return TOOL_DEFINITION_MAP.get(name);
}

export function registerToolDefinition(definition: MakoToolDefinition<string>): void {
  TOOL_DEFINITION_MAP.set(definition.name, definition);
}

export function unregisterToolDefinition(name: string): void {
  if ((TOOL_DEFINITIONS as readonly MakoToolDefinition[]).some((tool) => tool.name === name)) {
    throw new Error(`Cannot unregister built-in tool: ${name}`);
  }

  TOOL_DEFINITION_MAP.delete(name);
}
